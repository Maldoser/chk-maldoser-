<script>
    var audio = new Audio('live.mp3');
    var inicio = new Audio('ext.mp3');
    var notyf = new Notyf({
        duration: 3000,
        position: {
            x: 'right',
            y: 'top',
        },
    });
    $(document).ready(function(){

    notyf.success("<b>Limite máximo de 400 linhas.</b>")

    $('.show-lives').click(function(){
    var type = $('.show-lives').attr('type');
    $('#lista_aprovadas').slideToggle();
    if(type == 'show'){
    $('.show-lives').html('<i class="fa fa-eye"></i>');
    $('.show-lives').attr('type', 'hidden');
    }else{
    $('.show-lives').html('<i class="fa fa-eye-slash"></i>');
    $('.show-lives').attr('type', 'show');
    }});

    $('.show-dies').click(function(){
    var type = $('.show-dies').attr('type');
    $('#lista_reprovadas').slideToggle();
    if(type == 'show'){
    $('.show-dies').html('<i class="fa fa-eye"></i>');
    $('.show-dies').attr('type', 'hidden');
    }else{
    $('.show-dies').html('<i class="fa fa-eye-slash"></i>');
    $('.show-dies').attr('type', 'show');
    }});

    $('.btn-trash').click(function(){
        notyf.success("<b>Lista de reprovadas limpa!</b>")
    $('#lista_reprovadas').text('');
    });

    $('.btn-copy').click(function(){
        notyf.success("<b>Lista de Aprovadas copiada!</b>")
        var lista_lives = document.getElementById('lista_aprovadas').innerText;
        var textarea = document.createElement("textarea");
        textarea.value = lista_lives;
        document.body.appendChild(textarea); 
        textarea.select(); 
        document.execCommand('copy');
        document.body.removeChild(textarea); 
    });


    $('.btn-play').click(async function(){
    inicio.play();

    var lista = $('.form-checker').val().trim();
    var array = lista.split('\n');
    var lives = 0, dies = 0, testadas = 0, txt = '';

    if(!lista){
        notyf.error("<b>Insira uma lista válida</b>")
        return false;
    }

    notyf.success("<b>Testador iniciado!</b>")

    var line = array.filter(function(value){
    if(value.trim() !== ""){
        txt += value.trim() + '\n';
        return value.trim();
    }
    });

    var total = line.length;

    $('.form-checker').val(txt.trim());

    if(total > 400){
        notyf.error("<b>Limite máximo de linhas: 400</b>")
        return false;
    }

    $('.carregadas').text(total);
    $('.btn-play').attr('disabled', true);
    $('.btn-stop').attr('disabled', false);
    var cookie1 = document.getElementById("cookie1").value;
    for (var cc of line) {
        var sair;
        if (sair === false) {
            return false
        }
var callBack = $.ajax({
          url: 'api.php',
            type: "POST",
            data: {
        "lista": cc,
                "cookie1": cookie1,
            },
            success: function(retorno){
                if(retorno.indexOf("Aprovada") >= 0){
                    notyf.success("<b>+1 Aprovada!</b>")
                    $('#lista_aprovadas').append(retorno);
                    audio.play();
                    removelinha();
                    lives = lives +1;
                }else{
                    $('#lista_reprovadas').append(retorno);
                    removelinha();
                    dies = dies +1;
                }
                testadas = lives + dies;
                $('.aprovadas').text(lives);
                $('.reprovadas').text(dies);
                $('.testadas').text(testadas);
                
                if(testadas == total){
                    notyf.success("<b>Testador finalizado!</b>")
                    $('.btn-play').attr('disabled', false);
                    $('.btn-stop').attr('disabled', true);
                }
            }
        });
        $('.btn-stop').click(function(){
            notyf.error("<b>Testador pausado!</b>")
        $('.btn-play').attr('disabled', false);
        $('.btn-stop').attr('disabled', true);      
            callBack.abort();
            sair = false
            return false;
        });
    };
    });
    });

    function removelinha() {
    var lines = $('.form-checker').val().split('\n');
    lines.splice(0, 1);
    $('.form-checker').val(lines.join("\n"));
    }
</script>


